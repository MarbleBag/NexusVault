package nexusvault.archive;

public class PackIndexOutOfBounds extends PackException {

	private static final long serialVersionUID = 1L;

	public PackIndexOutOfBounds() {
		super();
	}

	public PackIndexOutOfBounds(String s) {
		super(s);
	}

	public PackIndexOutOfBounds(String message, Throwable cause) {
		super(message, cause);
	}

	public PackIndexOutOfBounds(Throwable cause) {
		super(cause);
	}

}