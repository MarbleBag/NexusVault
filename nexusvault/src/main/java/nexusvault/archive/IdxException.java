package nexusvault.archive;

public class IdxException extends ArchiveException {

	private static final long serialVersionUID = -8853975753798978854L;

	public IdxException() {
		super();
	}

	public IdxException(String s) {
		super(s);
	}

	public IdxException(String message, Throwable cause) {
		super(message, cause);
	}

	public IdxException(Throwable cause) {
		super(cause);
	}

}
