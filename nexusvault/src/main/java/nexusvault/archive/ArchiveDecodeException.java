package nexusvault.archive;

public final class ArchiveDecodeException extends ArchiveException {

	private static final long serialVersionUID = 5530467723975048345L;

	public ArchiveDecodeException() {
		super();
	}

	public ArchiveDecodeException(String s) {
		super(s);
	}

	public ArchiveDecodeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ArchiveDecodeException(Throwable cause) {
		super(cause);
	}

}
