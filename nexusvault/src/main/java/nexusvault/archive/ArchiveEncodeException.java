package nexusvault.archive;

public final class ArchiveEncodeException extends ArchiveException {

	private static final long serialVersionUID = 5530467723975048345L;

	public ArchiveEncodeException() {
		super();
	}

	public ArchiveEncodeException(String s) {
		super(s);
	}

	public ArchiveEncodeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ArchiveEncodeException(Throwable cause) {
		super(cause);
	}

}
