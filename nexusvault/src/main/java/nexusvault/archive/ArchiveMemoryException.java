package nexusvault.archive;

public final class ArchiveMemoryException extends ArchiveException {

	private static final long serialVersionUID = -9041606560452492543L;

	public ArchiveMemoryException() {
		super();
	}

	public ArchiveMemoryException(String s) {
		super(s);
	}

	public ArchiveMemoryException(String message, Throwable cause) {
		super(message, cause);
	}

	public ArchiveMemoryException(Throwable cause) {
		super(cause);
	}

}
